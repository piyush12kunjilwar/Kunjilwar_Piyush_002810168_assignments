    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblPersons.getModel();
        dtm.setRowCount(0);
        
        for(Person person : personHistory.getPersonHistory()){
            Object[] row = new Object[8];
            row[0] = person;
            row[1] = person.getLastName();
            row[2] = person.getWorkAddress().getCity(); // Work Address City
            row[3] = person.getWorkAddress().getZipCode(); // Work Address Zip Code
            row[4] = person.getWorkAddress().getStreetAddress();
            row[5] = person.getHomeAddress().getCity(); // Home Address City
            row[6] = person.getHomeAddress().getZipCode(); // Home Address Zip Code
            row[7] = person.getHomeAddress().getStreetAddress();
            
            dtm.addRow(row);
            
        }
 
    }

